interface LanguageContextType {
  language: AppLanguageCode;
  setLanguage: (lang: AppLanguageCode) => void;
  t: (key: string, fallback?: string) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<AppLanguageCode>(() => {
    return (localStorage.getItem('appLanguage') as AppLanguageCode) || 'en';
  });

  const setLanguage = (lang: AppLanguageCode) => {
    setLanguageState(lang);
    localStorage.setItem('appLanguage', lang);
  };

  const t = (key: string, fallback?: string): string => {
    const dict = allTranslations[language];
    if (dict && dict[key]) return dict[key];
    const enDict = allTranslations['en'];
    if (enDict && enDict[key]) return enDict[key];
    return fallback || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useTranslation must be used within a LanguageProvider');
  }
  return context;
};
